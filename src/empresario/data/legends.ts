export function getLegendById(id: string): Legend | undefined {
  return LEGENDS.find(l => l.id === id)
}

export function getAvailableLegends(year: number, seenIds: string[]): Legend[] {
  return LEGENDS.filter(l =>
    l.emergenceYear <= year + 2 &&
    l.birthYear <= year - 13 &&
    !seenIds.includes(l.id)
  )
}

export function getCurrentRating(legend: Legend, year: number): number {
  const yearsActive = Math.max(0, year - legend.emergenceYear)
  const peakDuration = legend.peakYearEnd - legend.peakYearStart
  const yearsIntoPeak = year - legend.peakYearStart

  if (year < legend.emergenceYear) {
    return Math.max(legend.currentRating - 10, 15)
  }

  if (year < legend.peakYearStart) {
    const progress = yearsActive / Math.max(1, legend.peakYearStart - legend.emergenceYear)
    return Math.round(legend.currentRating + (legend.truePotential - 20 - legend.currentRating) * Math.min(progress, 0.7))
  }

  if (year <= legend.peakYearEnd) {
    const peakProgress = Math.min(1, yearsIntoPeak / Math.max(1, peakDuration / 2))
    return Math.round(legend.truePotential - 5 + peakProgress * 5)
  }

  const decline = Math.min(30, (year - legend.peakYearEnd) * 4)
  return Math.max(55, legend.truePotential - decline)
}

export function getMarketValue(legend: Legend, year: number): number {
  const rating = getCurrentRating(legend, year)
  const base = Math.pow(rating / 50, 3) * 5000000
  const peakMultiplier = year >= legend.peakYearStart && year <= legend.peakYearEnd ? 1.8 : 1
  return Math.round(base * peakMultiplier / 1000) * 1000
}
